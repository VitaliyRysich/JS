console.log("Comparison Operators");
console.log(10 == 10);
console.log(10 < 10);
console.log(10 <= 10);
console.log(10 > 10);
console.log(10 >= 10);
console.log(10 != 10);

console.log("Logical Operators");
console.log(10 == 10 && 20 == 20); //and &&
console.log(10 == 10 && 20 != 20);

console.log(10 == 10 || 20 == 30); //or ||
console.log(20 == 30 || 10 == 10);
console.log(10 == 10 || 20 == 30);

console.log(!(10 == 10)); //not !
console.log(!(10 == 30));